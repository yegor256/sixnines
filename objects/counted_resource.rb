require 'uri'
require 'timeout'
require 'net/http'
require 'openssl'
require_relative '../objects/resource'

#
# Resource whose pings are counted
#
class CountedResource
  def initialize(resource, count)
    @resource = resource
    @count = count
  end

  def take(host = nil, port = nil)
    take = @resource.take(host, port)
    @count.increment(1)
    take
  end
end
